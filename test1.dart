// 1. เขียน code If - else 
    // กำหนดตัวแปร int ชื่อว่า age 
    // ถ้า age มากกว่า 50 พิมพ์ ว่า “Old”
    // ถ้า age มากกว่า 20 พิมพ์ ว่า “Medium”
    // ถ้า age น้อยกว่า 20 พิมพ์ ว่า “Young”

void main() {

int age = 100;
if (age > 50) {
  print("Old");
} else if (age > 20) {
  print("Medium");
} else {
  print("Young");
}
}